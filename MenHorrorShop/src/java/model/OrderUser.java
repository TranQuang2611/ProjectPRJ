package model;

import java.util.Date;

/**
 *
 * @author admin
 */
public class OrderUser {
    int id;
    Account account;
    String date;

    public OrderUser() {
    }

    public OrderUser(int id, Account account, String date) {
        this.id = id;
        this.account = account;
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Account getAccount() {
        return account;
    }

    public void setAccount(Account account) {
        this.account = account;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    
    
    
}
